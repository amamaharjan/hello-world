    public static String getTextVector(String data, String[]dataText){
        
        int position = getPositionVectorText(data, dataText);
        if(position==-1){
            return data;
        }
        return dataText[position];
    }
    
    public static void addTextAndSelectTextToReset(JTextField textField, String newData){
        
        String data = "";
        int actualNo = textField.getText().length();
        data = newData.substring(actualNo, newData.length());
        if(newData.isEmpty()||data.isEmpty()){
            return;
        }
        try{
            textField.getDocument().insertString(textField.getCaretPosition(), data, null);
        }catch(Exception e){
            
        }
        textField.select(actualNo, textField.getText().length());
        
    }
    
