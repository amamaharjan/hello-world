    public static int getPositionVectorText(String data, String[]dataText){
        
        try{
            for(int i=0; i<dataText.length; i++){
                
                if(data.equals(dataText[i].substring(0, data.length()))){
                    return i;
                }
                
            }
        }catch(Exception e){
            
        }
        return -1;
    }
    
    
    
